package com.yl.bean;

public class GuideClick {
	private Integer id;
	private Integer bgfId;
	private Integer clickNum;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public Integer getBgfId() {
		return bgfId;
	}

	public void setBgfId(Integer bgfId) {
		this.bgfId = bgfId;
	}

	public Integer getClickNum() {
		return clickNum;
	}

	public void setClickNum(Integer clickNum) {
		this.clickNum = clickNum;
	}

}
