package com.yl.bean;

public class ScenicSite {
	private Integer id;
	private String scenicSiteName;
	private Integer scenicId;

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getScenicSiteName() {
		return scenicSiteName;
	}

	public void setScenicSiteName(String scenicSiteName) {
		this.scenicSiteName = scenicSiteName;
	}

	public Integer getScenicId() {
		return scenicId;
	}

	public void setScenicId(Integer scenicId) {
		this.scenicId = scenicId;
	}

}
